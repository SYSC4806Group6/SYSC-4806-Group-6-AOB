package org.example.entities;

import jakarta.persistence.*;

import org.springframework.security.core.GrantedAuthority;
import org.springframework.security.core.authority.SimpleGrantedAuthority;
import org.springframework.security.core.userdetails.UserDetails;
import java.util.ArrayList;
import java.util.List;

@Entity
@Table(name = "users")
public class User implements UserDetails {
    @Id
    @GeneratedValue
    private long id;
    private String email;
    private String username;
    private String password;
    @Enumerated(EnumType.STRING)
    private USER_ROLE userRole;
    @OneToOne(mappedBy = "user", cascade = CascadeType.ALL)
    private ShoppingCart shoppingCart;
    @OneToMany(mappedBy = "user",  cascade = CascadeType.ALL)
    private List<PurchaseReceipt> purchaseReceipts;

    /**
     * Default Constructor
     */
    public User() {
        // Id AutoGenerated
        this.email = "N/A";
        this.username = "N/A";
        this.password = "N/A";
        this.userRole = USER_ROLE.CUSTOMER;
        this.shoppingCart = new ShoppingCart(this);
        this.purchaseReceipts = new ArrayList<PurchaseReceipt>();
    }

    /**
     * Constructor for initializing a User entity
     * @param email String of a user's email
     * @param username String of a user's username
     * @param password String of a user's password
     * @param userRole USER_ROLE of a user's role (customer or admin)
     */
    public User(String email, String username, String password, USER_ROLE userRole) {
        this.email = email;
        this.username = username;
        this.password = password;
        this.userRole = userRole;
        this.shoppingCart = new ShoppingCart(this);
        this.purchaseReceipts = new ArrayList<PurchaseReceipt>();
    }

    /**
     * add Purchase Receipt to User Account
     * @param purchaseReceipt PurchaseReceipt to be added
     * @return True if added, False Otherwise
     */
    public boolean addPurchaseReceipt(PurchaseReceipt purchaseReceipt) {
        purchaseReceipt.setUser(this);
        return this.purchaseReceipts.add(purchaseReceipt);
    }
    /**
     * remove Purchase Receipt to User Account
     * @param purchaseReceipt PurchaseReceipt to be added
     * @return True if removed, False otherwise
     */
    public boolean removePurchaseReceipt(PurchaseReceipt purchaseReceipt) {
        purchaseReceipt.setUser(null);
        return this.purchaseReceipts.remove(purchaseReceipt);
    }

    /* Getters and setters */
    public long getId() {
        return this.id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getEmail() {
        return this.email;
    }
    public void setEmail(String email) {
        this.email = email;
    }
    public String getUsername() {
        return this.username;
    }
    public void setUsername(String username) {
        this.username = username;
    }
    public String getPassword() {
        return this.password;
    }
    public void setPassword(String password) {
        this.password = password;
    }
    public USER_ROLE getUserRole() {
        return this.userRole;
    }
    public void setUserRole(USER_ROLE userRole) {
        this.userRole = userRole;
    }
    public ShoppingCart getShoppingCart() {
        return this.shoppingCart;
    }
    public void setShoppingCart(ShoppingCart shoppingCart) {
        this.shoppingCart = shoppingCart;
    }
    public List<PurchaseReceipt> getPurchaseReceipts() {
        return this.purchaseReceipts;
    }
    public void setPurchaseReceipts(List<PurchaseReceipt> purchaseReceipts) {
        this.purchaseReceipts = purchaseReceipts;
    }

    @Override
    public java.util.Collection<? extends GrantedAuthority> getAuthorities() {
        // We MUST prefix the role with "ROLE_" for Spring's .hasRole() checks to work
        SimpleGrantedAuthority authority = new SimpleGrantedAuthority("ROLE_" + this.userRole.name());
        return java.util.List.of(authority);
    }

    /**
     * These methods are also required by UserDetails.
     * For now, we can just return 'true' to indicate that
     * all accounts are active and valid by default.
     */
    @Override
    public boolean isAccountNonExpired() {
        return true;
    }

    @Override
    public boolean isAccountNonLocked() {
        return true;
    }

    @Override
    public boolean isCredentialsNonExpired() {
        return true;
    }

    @Override
    public boolean isEnabled() {
        return true;
    }

    /**
     * Consider equal if id is the same
     * @param o   the reference object with which to compare.
     * @return True if equal, False otherwise
     */
    @Override
    public boolean equals(Object o) {
        if (this == o) {
            return true;
        }
        if (o == null || getClass() != o.getClass()) {
            return false;
        }
        User u = (User) o;
        return (this.id == u.id);
    }
}
